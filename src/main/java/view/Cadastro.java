    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        // Botao Cadastrar
        if ((user.getText().isEmpty()) || (nome.getText().isEmpty()) ||
            (senha.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Os campos não podem retornar vazios");
            } else {
       
            UsuarioDAO dao = new UsuarioDAO();
            UsuarioBean novoUsuario = new UsuarioBean();
            novoUsuario.setNome(nome.getText());
            novoUsuario.setUsuario(user.getText());
            novoUsuario.setSenha(senha.getText());
            dao.cadastrar(novoUsuario);

            JOptionPane.showMessageDialog(null, "Usuário "+ user.getText()+" inserido com sucesso! ");  
            }
            nome.setText("");
            user.setText("");
            senha.setText("");
            
            // chamando método para validarEmail
            String email = user.getText();
            
            if (!validarEmail(email)) {
                JOptionPane.showMessageDialog(null, "Email inválido!");
                user.requestFocus();
                return;
            }
            
            Login telaLogin = new Login();
            
            telaLogin.setVisible(true);
            this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

